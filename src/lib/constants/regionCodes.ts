/**
 * 대한민국 전국 시도/시군구 법정동 코드 상수.
 * - 각 시군구의 `code` 는 국토부 API 의 LAWD_CD(법정동 코드 5자리)이다.
 * - 강원/전북은 특별자치도 전환으로 코드가 변경됨(강원 51, 전북 52) — 라이브 API 로 검증함.
 * - `dongCodes` 는 동 단위 필터링용 보조값으로, 대표 구(강남/서초/송파)만 채워두었다.
 */

/** 시군구 단위 코드 정보 */
export interface DistrictCode {
  /** 법정동 코드 5자리 (LAWD_CD) */
  readonly code: string;
  /** 읍면동명 → 동 코드(5자리) 매핑 (선택적, 비어 있을 수 있음) */
  readonly dongCodes: Readonly<Record<string, string>>;
}

/** 시도 단위 코드 정보 */
export interface SidoCode {
  /** 시도 코드 2자리 */
  readonly code: string;
  /** 시군구명 → 시군구 코드 정보 매핑 */
  readonly districts: Readonly<Record<string, DistrictCode>>;
}

/** dongCodes 가 없는 구의 기본값 (빈 객체) */
const EMPTY_DONG: Readonly<Record<string, string>> = {};

/** code 만 있는 시군구를 간단히 생성 (dongCodes 비움) */
function d(code: string): DistrictCode {
  return { code, dongCodes: EMPTY_DONG };
}

