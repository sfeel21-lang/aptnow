/**
 * 등록된 시도명 목록을 반환한다.
 */
export function getSidoList(): string[] {
  return Object.keys(REGION_CODES);
}

/**
 * 특정 시도에 속한 시군구명 목록을 반환한다.
 * @param sido 시도명 (예: "서울특별시")
 * @returns 시군구명 배열 (시도 미존재 시 빈 배열)
 */
export function getGugunList(sido: string): string[] {
  const entry = REGION_CODES[sido];
  return entry ? Object.keys(entry.districts) : [];
}

/**
 * 특정 시도/시군구에 등록된 읍면동명 목록을 반환한다.
 * @param sido 시도명 (예: "서울특별시")
 * @param gugun 시군구명 (예: "강남구")
 * @returns 읍면동명 배열 (없으면 빈 배열)
 */
export function getDongList(sido: string, gugun: string): string[] {
  const dongCodes = REGION_CODES[sido]?.districts[gugun]?.dongCodes;
  return dongCodes ? Object.keys(dongCodes) : [];
}

/**
 * 법정동 코드(LAWD_CD)로 시도/시군구명을 역조회한다.
 * @param lawdCd LAWD_CD 5자리 (예: "11680")
 * @returns { sido, gugun } 객체 (미존재 시 null)
 */
export function getRegionByLawdCd(
  lawdCd: string,
): { sido: string; gugun: string } | null {
  for (const [sido, sidoData] of Object.entries(REGION_CODES)) {
    for (const [gugun, district] of Object.entries(sidoData.districts)) {
      if (district.code === lawdCd) return { sido, gugun };
    }
  }
  return null;
}

/**
 * 시도/시군구명으로 법정동 코드(LAWD_CD)를 반환한다.
 * @param sido 시도명 (예: "서울특별시")
 * @param gugun 시군구명 (예: "강남구")
 * @returns LAWD_CD 5자리 (미존재 시 빈 문자열)
 */
export function getLawdCd(sido: string, gugun: string): string {
  return REGION_CODES[sido]?.districts[gugun]?.code ?? "";
}
